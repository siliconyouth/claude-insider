export function DashboardNav({ isAdmin }: DashboardNavProps) {
  const pathname = usePathname();

  const filteredItems = navItems.filter((item) => !item.adminOnly || isAdmin);

  return (
    <nav className="space-y-1">
      {filteredItems.map((item) => {
        const isActive =
          item.href === "/dashboard"
            ? pathname === "/dashboard"
            : pathname.startsWith(item.href);

        return (
          <Link
            key={item.href}
            href={item.href}
            className={cn(
              "flex items-center gap-3 px-4 py-3 rounded-lg text-sm font-medium transition-all",
              isActive
                ? "bg-gradient-to-r from-violet-600/20 via-blue-600/20 to-cyan-600/20 text-white border border-blue-500/30"
                : "text-gray-400 hover:text-white hover:bg-gray-800/50"
            )}
          >
            <span className={cn(isActive ? "text-cyan-400" : "text-gray-500")}>
              {item.icon}
            </span>
            {item.label}
          </Link>
        );
      })}
    </nav>
  );
}
