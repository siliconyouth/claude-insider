export function DashboardNav({ isAdmin }: DashboardNavProps) {
  const pathname = usePathname();

  return (
    <nav className="space-y-6">
      {navSections.map((section) => {
        // Filter items based on admin role
        const filteredItems = section.items.filter(
          (item) => !item.adminOnly || isAdmin
        );

        // Don't render empty sections
        if (filteredItems.length === 0) return null;

        return (
          <div key={section.title}>
            {/* Section header */}
            <h3 className="px-4 mb-2 text-xs font-semibold uppercase tracking-wider text-gray-500">
              {section.title}
            </h3>

            {/* Section items */}
            <div className="space-y-1">
              {filteredItems.map((item) => {
                const isActive =
                  item.href === "/dashboard"
                    ? pathname === "/dashboard"
                    : pathname.startsWith(item.href);

                return (
                  <Link
                    key={item.href}
                    href={item.href}
                    className={cn(
                      "flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium transition-all",
                      isActive
                        ? "bg-gradient-to-r from-violet-600/20 via-blue-600/20 to-cyan-600/20 text-white border border-blue-500/30"
                        : "text-gray-400 hover:text-white hover:bg-gray-800/50"
                    )}
                  >
                    <span className={cn(isActive ? "text-cyan-400" : "text-gray-500")}>
                      {item.icon}
                    </span>
                    {item.label}
                  </Link>
                );
              })}
            </div>
          </div>
        );
      })}
    </nav>
  );
}
