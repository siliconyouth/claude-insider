export function SocialInput({
  platform,
  value,
  onChange,
  className,
}: SocialInputProps) {
  const [error, setError] = useState<string | null>(null);
  const [isTouched, setIsTouched] = useState(false);

  const platformConfig = SOCIAL_PLATFORMS.find((p) => p.id === platform);

  const handleBlur = useCallback(() => {
    setIsTouched(true);
    if (value) {
      const result = validateSocialLink(platform, value);
      if (!result.isValid) {
        setError(result.error || "Invalid format");
      } else {
        setError(null);
        // Update with sanitized value
        if (result.sanitized && result.sanitized !== value) {
          onChange(result.sanitized);
        }
      }
    } else {
      setError(null);
    }
  }, [platform, value, onChange]);

  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    onChange(e.target.value);
    // Clear error while typing
    if (error) setError(null);
  };

  return (
    <div className={cn("space-y-1", className)}>
      <div className="relative">
        {/* Platform icon */}
        <div className="absolute left-3 top-1/2 -translate-y-1/2 text-gray-400">
          {PlatformIcons[platform]}
        </div>

        <input
          type="text"
          value={value}
          onChange={handleChange}
          onBlur={handleBlur}
          placeholder={platformConfig?.placeholder}
          className={cn(
            "w-full pl-11 pr-4 py-2.5 rounded-lg",
            "bg-white dark:bg-gray-900",
            "border text-gray-900 dark:text-white",
            "placeholder-gray-400 dark:placeholder-gray-500",
            "focus:outline-none focus:ring-2 focus:border-transparent",
            "transition-colors duration-200",
            error && isTouched
              ? "border-red-500 focus:ring-red-500/50"
              : "border-gray-200 dark:border-gray-700 focus:ring-blue-500"
          )}
        />

        {/* Validation indicator */}
        {isTouched && value && (
          <div className="absolute right-3 top-1/2 -translate-y-1/2">
            {error ? (
              <svg
                className="w-5 h-5 text-red-500"
                fill="none"
                stroke="currentColor"
                viewBox="0 0 24 24"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  strokeWidth={2}
                  d="M12 8v4m0 4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z"
                />
              </svg>
            ) : (
              <svg
                className="w-5 h-5 text-emerald-500"
                fill="none"
                stroke="currentColor"
                viewBox="0 0 24 24"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  strokeWidth={2}
                  d="M5 13l4 4L19 7"
                />
              </svg>
            )}
          </div>
        )}
      </div>

      {/* Error message */}
      {error && isTouched && (
        <p className="text-xs text-red-500 pl-1">{error}</p>
      )}
    </div>
  );
}
